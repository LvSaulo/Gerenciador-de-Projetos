public class SuperPA {

    public String identificacao, descricao, data_i, hora_i, data_t, hora_t;

    public SuperPA() {

    }
    public SuperPA(String identificacao, String descricao, String data_i, String data_t, String hora_i, String hora_t) {
        this.identificacao = identificacao;
        this.descricao = descricao;
        this.data_i = data_i;
        this.data_t = data_t;
        this.hora_i = hora_i;
        this.hora_t = hora_t;
    }
}
